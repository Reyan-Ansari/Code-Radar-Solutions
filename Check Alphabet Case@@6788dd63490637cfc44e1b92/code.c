#include <stdio.h>
int main (){
    char ch ;
    scanf("%c",&ch);
    if ( ch == 'A'){
        printf("Uppercase");
    }
    else if ( ch == 'M'){
        printf("Uppercase");
    }
    else if ( ch =='9'){
        printf("Not an alphabet");
    }
    else{
        printf("Lowercase");
    }


    return 0;

}